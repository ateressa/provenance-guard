@app.route("/appeal", methods=["POST"])
def appeal():
    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Request body must be JSON."}), 400

    content_id = body.get("content_id", "").strip()
    creator_reasoning = body.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "Missing required field: content_id"}), 400
    if len(creator_reasoning) < 10:
        return jsonify({"error": "creator_reasoning must be at least 10 characters."}), 400

    record = store.get(content_id)
    if record is None:
        return jsonify({"error": f"content_id '{content_id}' not found."}), 404

    store.update_status(content_id, "under_review")

    auditor.log_appeal(
        content_id=content_id,
        creator_reasoning=creator_reasoning,
        original_attribution=record["attribution"],
        original_confidence=record["confidence"],
    )

    return jsonify({
        "content_id": content_id,
        "status": "under_review",
        "original_attribution": record["attribution"],
        "original_confidence": record["confidence"],
        "message": "Your appeal has been received and flagged for human review. No automated re-classification will occur.",
    }), 200


