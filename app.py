@app.route("/analytics", methods=["GET"])
def analytics():
    """
    Aggregate statistics over all audit log entries.

    Returns:
    - total_submissions: classification events logged
    - attribution_breakdown: count + percentage per attribution category
    - appeal_rate: appeals / total_submissions
    - avg_confidence_by_attribution: mean confidence for each attribution category
    - certificates_issued: count of provenance certificates
    - content_type_breakdown: text vs code submissions
    """
    entries = auditor.get_log(limit=10000)

    classifications = [e for e in entries if e.get("event_type") == "classification"]
    appeals = [e for e in entries if e.get("event_type") == "appeal"]
    certs = [e for e in entries if e.get("event_type") == "certificate_issued"]

    total = len(classifications)
    appeal_count = len(appeals)

    breakdown: dict[str, dict] = {}
    confidence_sums: dict[str, list] = {}
    type_counts: dict[str, int] = {}

    for c in classifications:
        attr = c.get("attribution", "unknown")
        breakdown[attr] = breakdown.get(attr, {"count": 0})
        breakdown[attr]["count"] += 1

        confidence_sums.setdefault(attr, []).append(c.get("confidence", 0))

        ctype = c.get("content_type", "text")
        type_counts[ctype] = type_counts.get(ctype, 0) + 1

    for attr, data in breakdown.items():
        data["pct"] = round(data["count"] / total * 100, 1) if total else 0

    avg_confidence = {
        attr: round(sum(scores) / len(scores), 4)
        for attr, scores in confidence_sums.items()
    }

    return jsonify({
        "total_submissions": total,
        "attribution_breakdown": breakdown,
        "avg_confidence_by_attribution": avg_confidence,
        "appeal_rate": round(appeal_count / total, 4) if total else 0,
        "appeals_filed": appeal_count,
        "certificates_issued": len(certs),
        "content_type_breakdown": type_counts,
    }), 200


