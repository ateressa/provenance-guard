@app.route("/certify", methods=["POST"])
def certify():
    """
    Issue a 'Verified Human' certificate to a creator.

    The creator provides a short written statement about their creative process.
    The system runs that statement through the detection pipeline. If it scores
    as human (confidence <= CERT_MAX_CONFIDENCE), a certificate is issued and
    attached to the original content record.

    This does not re-classify the original content — it adds a creator-verified
    layer on top of the system's existing attribution.
    """
    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Request body must be JSON."}), 400

    content_id = body.get("content_id", "").strip()
    creator_statement = body.get("creator_statement", "").strip()

    if not content_id:
        return jsonify({"error": "Missing required field: content_id"}), 400
    if len(creator_statement) < 30:
        return jsonify({"error": "creator_statement must be at least 30 characters."}), 400

    record = store.get(content_id)
    if record is None:
        return jsonify({"error": f"content_id '{content_id}' not found."}), 404

    if record.get("attribution") == "ai_generated":
        return jsonify({
            "error": "Certificates cannot be issued for content classified as ai_generated. File an appeal first.",
            "content_id": content_id,
        }), 422

    if record.get("certificate"):
        return jsonify({
            "message": "A certificate has already been issued for this content.",
            "certificate": record["certificate"],
        }), 200

    # Run creator statement through detection pipeline
    stmt_llm = classify_llm(creator_statement)
    stmt_stylo = classify_stylometric(creator_statement)
    stmt_lex = classify_lexical(creator_statement)
    stmt_confidence = combine_scores(stmt_llm["score"], stmt_stylo["score"], stmt_lex["score"])

    if stmt_confidence > CERT_MAX_CONFIDENCE:
        return jsonify({
            "error": "Certificate denied. Your written statement did not show sufficient human writing signals.",
            "statement_confidence": round(stmt_confidence, 4),
            "threshold": CERT_MAX_CONFIDENCE,
            "tip": "Write naturally about your creative process — describe your inspiration, drafting experience, or specific decisions you made. Formal or structured statements may score as AI-like.",
        }), 422

    certificate_id = "cert-" + str(uuid.uuid4())[:8]
    certificate = {
        "certificate_id": certificate_id,
        "content_id": content_id,
        "creator_id": record["creator_id"],
        "issued_at": auditor._now(),
        "statement_confidence": round(stmt_confidence, 4),
        "display_text": (
            "Verified Human — This content has been verified as human-authored through "
            "Provenance Guard's creator verification process. The creator provided a written "
            f"statement that scored {round((1 - stmt_confidence) * 100)}% human confidence."
        ),
    }

    store.attach_certificate(content_id, certificate)
    auditor.log_certificate(
        content_id=content_id,
        creator_id=record["creator_id"],
        certificate_id=certificate_id,
        statement_confidence=stmt_confidence,
    )

    return jsonify({
        "content_id": content_id,
        "status": "verified_human",
        "certificate": certificate,
    }), 200


