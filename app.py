@app.route("/submit", methods=["POST"])
@limiter.limit(RATE_LIMIT)
def submit():
    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Request body must be JSON."}), 400

    text = body.get("text", "").strip()
    creator_id = body.get("creator_id", "").strip()
    content_type = body.get("content_type", "text").strip().lower()

    if not text:
        return jsonify({"error": "Missing required field: text"}), 400
    if not creator_id:
        return jsonify({"error": "Missing required field: creator_id"}), 400
    if content_type not in VALID_CONTENT_TYPES:
        return jsonify({"error": f"content_type must be one of: {sorted(VALID_CONTENT_TYPES)}"}), 400

    content_id = body.get("content_id") or str(uuid.uuid4())[:8]
    word_count = len(text.split())
    text_too_short = word_count < MIN_RELIABLE_WORDS

    # --- Detection pipeline (branches by content_type) ---
    if content_type == "code":
        llm_result = classify_code_llm(text)
        stylo_result = classify_code_stylometric(text)
    else:
        llm_result = classify_llm(text)
        stylo_result = classify_stylometric(text)

    llm_score = llm_result["score"]
    stylo_score = stylo_result["score"]

    # Signal 3: lexical (text only — not meaningful for code)
    if content_type == "text":
        lex_result = classify_lexical(text)
        lex_score = lex_result["score"]
    else:
        lex_result = {"score": 0.5, "features": {"note": "not applicable for code"}}
        lex_score = 0.5

    confidence = combine_scores(llm_score, stylo_score, lex_score)
    attribution = attribution_from_score(confidence)
    label = _build_label(confidence)

    record = {
        "content_id": content_id,
        "creator_id": creator_id,
        "content_type": content_type,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": round(llm_score, 4),
        "stylometric_score": round(stylo_score, 4),
        "lexical_score": round(lex_score, 4),
        "text_too_short": text_too_short,
        "status": "classified",
        "llm_reasoning": llm_result["reasoning"],
    }
    store.save(content_id, record)

    auditor.log_classification(
        content_id=content_id,
        creator_id=creator_id,
        attribution=attribution,
        confidence=confidence,
        llm_score=llm_score,
        stylometric_score=stylo_score,
        lexical_score=lex_score,
        content_type=content_type,
        text_too_short=text_too_short,
    )

    response = {
        "content_id": content_id,
        "content_type": content_type,
        "attribution": attribution,
        "confidence": confidence,
        "signals": {
            "llm_judge": {
                "score": round(llm_score, 4),
                "label": llm_result["label"],
                "reasoning": llm_result["reasoning"],
            },
            "stylometric": {
                "score": round(stylo_score, 4),
                "features": stylo_result.get("features"),
            },
            "lexical": {
                "score": round(lex_score, 4),
                "features": lex_result.get("features"),
            },
        },
        "label": label,
        "text_too_short": text_too_short,
    }
    if text_too_short:
        response["warning"] = f"Text is {word_count} words. Scores are unreliable below {MIN_RELIABLE_WORDS} words."

    return jsonify(response), 200


